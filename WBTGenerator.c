void shiftRows(unsigned char out[16]){
    
    // +----+----+----+----+
    // | 00 | 04 | 08 | 12 |
    // +----+----+----+----+
    // | 01 | 05 | 09 | 13 |
    // +----+----+----+----+
    // | 02 | 06 | 10 | 14 |
    // +----+----+----+----+
    // | 03 | 07 | 11 | 15 |
    // +----+----+----+----+
    
    int tmp;
    unsigned int i,k,s;
    
    for(i=1; i<4; i++){
        s=0;
        while(s<i){
            tmp=out[i];
            for(k=0; k<3; k++){
                out[k*4+i]=out[k*4+i+4];
            }
            
            out[i+12]=tmp;
            s++;
        }
    }
}

void mixColumns(unsigned char out[16]) {
    for (size_t i = 0; i < 4; ++i) {
        unsigned char x = out[i * 4 + 0];
        unsigned char y = out[i * 4 + 1];
        unsigned char z = out[i * 4 + 2];
        unsigned char k = out[i * 4 + 3];

        out[i * 4 + 0] = gmul[matrix[0]][x]  ^ gmul[matrix[1]][y]  ^ gmul[matrix[2]][z]  ^ gmul[matrix[3]][k];
        out[i * 4 + 1] = gmul[matrix[4]][x]  ^ gmul[matrix[5]][y]  ^ gmul[matrix[6]][z]  ^ gmul[matrix[7]][k];
        out[i * 4 + 2] = gmul[matrix[8]][x]  ^ gmul[matrix[9]][y]  ^ gmul[matrix[10]][z] ^ gmul[matrix[11]][k];
        out[i * 4 + 3] = gmul[matrix[12]][x] ^ gmul[matrix[13]][y] ^ gmul[matrix[14]][z] ^ gmul[matrix[15]][k];
    }
}

